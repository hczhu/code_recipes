# ---------------------------------------------------------------------------
# Little-Endian NBT parser (Bedrock format)
# ---------------------------------------------------------------------------
TAG_END, TAG_BYTE, TAG_SHORT, TAG_INT, TAG_LONG = 0, 1, 2, 3, 4
TAG_FLOAT, TAG_DOUBLE, TAG_BYTEARRAY, TAG_STRING = 5, 6, 7, 8
TAG_LIST, TAG_COMPOUND, TAG_INTARRAY, TAG_LONGARRAY = 9, 10, 11, 12


class NBTReader:
    """Reads little-endian NBT from a bytes buffer, tracking the current offset."""

    def __init__(self, data: bytes, offset: int = 0):
        self.data = data
        self.offset = offset

    # --- primitives ---
    def _adv(self, n: int) -> int:
        o = self.offset
        if o + n > len(self.data):
            raise struct.error(
                f"buffer too small: need {n} byte(s) at offset {o}, "
                f"only {len(self.data) - o} remain"
            )
        self.offset += n
        return o

    def u8(self)  -> int: return struct.unpack_from('<B', self.data, self._adv(1))[0]
    def i8(self)  -> int: return struct.unpack_from('<b', self.data, self._adv(1))[0]
    def i16(self) -> int: return struct.unpack_from('<h', self.data, self._adv(2))[0]
    def u16(self) -> int: return struct.unpack_from('<H', self.data, self._adv(2))[0]
    def i32(self) -> int: return struct.unpack_from('<i', self.data, self._adv(4))[0]
    def i64(self) -> int: return struct.unpack_from('<q', self.data, self._adv(8))[0]
    def f32(self) -> float: return struct.unpack_from('<f', self.data, self._adv(4))[0]
    def f64(self) -> float: return struct.unpack_from('<d', self.data, self._adv(8))[0]

    def string(self) -> str:
        n = self.u16()
        raw = self.data[self.offset:self.offset + n]
        self.offset += n
        return raw.decode('utf-8', errors='replace')

    def read_payload(self, tag: int):
        if tag == TAG_END:       return None
        if tag == TAG_BYTE:      return self.i8()
        if tag == TAG_SHORT:     return self.i16()
        if tag == TAG_INT:       return self.i32()
        if tag == TAG_LONG:      return self.i64()
        if tag == TAG_FLOAT:     return self.f32()
        if tag == TAG_DOUBLE:    return self.f64()
        if tag == TAG_BYTEARRAY:
            n = self.i32()
            if n < 0 or n > len(self.data):
                raise ValueError(f"TAG_BYTEARRAY size {n} out of range")
            b = self.data[self.offset:self.offset + n]
            self.offset += n
            return b
        if tag == TAG_STRING:    return self.string()
        if tag == TAG_LIST:
            et = self.u8()
            c  = self.i32()
            if c < 0 or c > 65536:
                raise ValueError(f"TAG_LIST count {c} out of range")
            return [self.read_payload(et) for _ in range(c)]
        if tag == TAG_COMPOUND:
            d = {}
            while True:
                t = self.u8()
                if t == TAG_END:
                    break
                name = self.string()
                d[name] = self.read_payload(t)
            return d
        if tag == TAG_INTARRAY:
            n = self.i32()
            if n < 0 or n > 65536:
                raise ValueError(f"TAG_INTARRAY count {n} out of range")
            return [self.i32() for _ in range(n)]
        if tag == TAG_LONGARRAY:
            n = self.i32()
            if n < 0 or n > 65536:
                raise ValueError(f"TAG_LONGARRAY count {n} out of range")
            return [self.i64() for _ in range(n)]
        raise ValueError(f"Unknown NBT tag type: {tag}")

    def parse_root(self):
        """Parse one root NBT value (tag byte + name + payload)."""
        tag  = self.u8()
        name = self.string()
        return name, self.read_payload(tag)


def parse_nbt(data: bytes, offset: int = 0):
    """Return (nbt_dict, new_offset). Returns (None, offset) on error."""
    try:
        r = NBTReader(data, offset)
        _, nbt = r.parse_root()
        return nbt, r.offset
    except Exception:
        return None, offset


# ---------------------------------------------------------------------------
# Subchunk decoder
# ---------------------------------------------------------------------------
def decode_subchunk_blocks(data: bytes, chunk_x: int, chunk_z: int, sub_y_signed: int):
    """
    Decode a Bedrock subchunk (version 8 or 9) and yield
    (world_x, world_y, world_z, block_name) for every non-air block
    whose name is NOT in NATURAL_BLOCKS.

    sub_y_signed : signed sub-chunk Y index (world_y_base = sub_y_signed * 16)
    """
    if not data or len(data) < 3:
        return

    version = data[0]
    if version not in (8, 9):
        # Version 1 (legacy) or unknown — skip detailed decode
        return

    num_layers = data[1]
    if num_layers == 0:
        return

    # Version 9 stores the sub-chunk Y index as an extra byte at offset 2
    # before the storage layer data, so skip it.
    offset = 3 if version == 9 else 2
    base_y = sub_y_signed * 16

    # We only need the first (primary) storage layer
    storage_header = data[offset]; offset += 1
    bits_per_block = storage_header >> 1
    # bit 0 = is_network_palette (should be 0 for disk format)
    # Valid disk values: 0,1,2,3,4,5,6,8,16  — reject anything larger
    if bits_per_block > 16:
        return

    try:
        if bits_per_block == 0:
            # All 4096 blocks are the single palette entry (palette[0]).
            # Format: palette_size (int32, always 1) + 1 NBT compound.
            # NOTE: there is NO packed word array when bits_per_block == 0.
            if offset + 4 > len(data):
                return
            palette_size = struct.unpack_from('<i', data, offset)[0]; offset += 4
            if palette_size < 1 or palette_size > 4096:
                return
            nbt, offset = parse_nbt(data, offset)
            block_name = nbt.get('name', 'minecraft:air') if nbt else 'minecraft:air'
            if block_name not in NATURAL_BLOCKS:
                # Every one of the 4096 blocks in this subchunk is non-natural
                for idx in range(4096):
                    lx = idx % 16
                    lz = (idx // 16) % 16
                    ly = idx // 256
                    yield (chunk_x * 16 + lx,
                           base_y + ly,
                           chunk_z * 16 + lz,
                           block_name)
        else:
            blocks_per_word = 32 // bits_per_block
            word_count = math.ceil(4096 / blocks_per_word)

            if offset + word_count * 4 > len(data):
                return

            words = struct.unpack_from(f'<{word_count}I', data, offset)
            offset += word_count * 4

            # Unpack block indices from bit-packed words
            mask = (1 << bits_per_block) - 1
            indices = []
            for word in words:
                for shift in range(0, 32, bits_per_block):
                    indices.append((word >> shift) & mask)
                    if len(indices) == 4096:
                        break
                if len(indices) == 4096:
                    break

            # Read palette
            if offset + 4 > len(data):
                return
            palette_size = struct.unpack_from('<i', data, offset)[0]; offset += 4
            if palette_size < 1 or palette_size > 4096:
                return
            palette = []
            for _ in range(palette_size):
                nbt, offset = parse_nbt(data, offset)
                palette.append(nbt.get('name', 'minecraft:air') if nbt else 'minecraft:air')

            if not palette:
                return

            # Yield non-natural blocks
            for idx, pal_idx in enumerate(indices):
                if pal_idx >= len(palette):
                    continue
                block_name = palette[pal_idx]
                if block_name in NATURAL_BLOCKS:
                    continue
                lx = idx % 16
                lz = (idx // 16) % 16
                ly = idx // 256
                yield (chunk_x * 16 + lx,
                       base_y + ly,
                       chunk_z * 16 + lz,
                       block_name)

    except Exception:
        return


# ---------------------------------------------------------------------------
# Entity decoder
# ---------------------------------------------------------------------------
def decode_entity(data: bytes):
    """
    Parse an actorprefix NBT record and return (entity_id, x, y, z) or None.
    """
    nbt, _ = parse_nbt(data)
    if not isinstance(nbt, dict):
        return None
    eid = nbt.get('id') or nbt.get('identifier')
    if not isinstance(eid, str):
        return None
    pos = nbt.get('Pos')
    if isinstance(pos, list) and len(pos) >= 3:
        return eid, pos[0], pos[1], pos[2]
    return None


