def ordinal(n: int) -> str:
    """Return '1st', '2nd', '3rd', '4th', … for a positive integer."""
    if 11 <= (n % 100) <= 13:
        return f"{n}th"
    return f"{n}" + {1: "st", 2: "nd", 3: "rd"}.get(n % 10, "th")


def open_db_readonly(db_path: str) -> LevelDB:
    """
    Open a Bedrock LevelDB without acquiring the exclusive LOCK file.
    Standard LevelDB has no built-in read-only mode, so we temporarily
    move any existing LOCK file aside, open the DB (which creates a new
    LOCK), immediately remove that new LOCK, then restore the original.
    This lets the script run alongside a live server on a different world
    copy, or against an idle backup without interference.
    """
    lock_path   = os.path.join(db_path, "LOCK")
    backup_path = lock_path + ".scan_bak"

    original_existed = os.path.exists(lock_path)
    if original_existed:
        shutil.move(lock_path, backup_path)

    try:
        db = LevelDB(db_path, create_if_missing=False)
        # Remove the new LOCK our open just created — we won't be writing.
        if os.path.exists(lock_path):
            os.remove(lock_path)
    finally:
        # Always restore the original lock (if there was one).
        if original_existed and os.path.exists(backup_path):
            if os.path.exists(lock_path):
                os.remove(lock_path)
            shutil.move(backup_path, lock_path)

    return db


